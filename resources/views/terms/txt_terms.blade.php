	<div class="terms_tit">제 1장 총칙</div>

	<div class="terms_tt">제1조 (목적)</div>
	<p>이 약관은 전국대학연구·산학협력 관리자협의회(이하 “협의회”)가 운영하는 교육연수시스템(이하 “시스템”)에서 제공하는 교육신청, 수강, 수료증 발급 등 각종 연수 관련 서비스의 이용에 관한 회원과 협의회의 권리·의무 및 책임사항, 기타 필요한 사항을 규정함을 목적으로 합니다.</p>
	   
	<div class="terms_tt">제2조 (정의)</div>
	<p>이 약관에서 사용하는 용어의 정의는 다음과 같습니다.</p>
	<ol>
		<li>① “교육연수시스템”이라 함은 산학협력단 실무자 및 대학 관계자를 대상으로 교육과정 신청, 연수 운영, 수료증 발급 등을 관리하기 위한 온라인 서비스 플랫폼을 말합니다.</li>
		<li>② “운영기관”이라 함은 전국대학연구·산학협력 관리자협의회로서, 본 시스템을 관리·운영하며 교육 관련 정책 및 기술적 지원을 수행하는 기관을 말합니다.</li>
		<li>③ “서비스”라 함은 회원이 시스템을 통하여 이용할 수 있는 교육신청, 수료증 발급, 교육정보 열람 및 관련 부가서비스 일체를 말합니다.</li>
		<li>④ “회원”이라 함은 본 약관에 동의하고 시스템에 가입하여 서비스를 이용하는 산학협력단 실무자, 대학 관계자, 또는 협의회가 인정한 이용자를 말합니다.</li>
		<li>⑤ “아이디(ID)”라 함은 회원 식별 및 서비스 이용을 위하여 회원이 정한 문자와 숫자의 조합을 의미합니다.</li>
		<li>⑥ “비밀번호”라 함은 회원이 부여받은 아이디와 일치되는 회원임을 확인하고 비밀보호를 위해 회원이 정한 문자 또는 숫자의 조합을 의미합니다.</li>
		<li>⑦ “게시물”이라 함은 회원이 서비스 이용 중 시스템 상에 게시한 글, 사진, 동영상, 파일 및 링크 등 일체의 콘텐츠를 말합니다.</li>
	</ol>
	   
	<div class="terms_tt">제3조 (이용계약의 체결)</div>
	<ol>
		<li>① 이용계약은 회원이 되고자 하는 자(이하 “가입신청자”)가 약관의 내용에 동의하고 회원가입 절차를 완료함으로써 체결됩니다.</li>
		<li>② 운영기관은 필요 시 본인확인을 위해 인증기관을 통한 본인확인 절차를 요구할 수 있습니다.</li>
		<li>③ 운영기관은 기술적, 업무적 사유가 있는 경우 가입신청 승낙을 유보할 수 있으며, 그 사유를 신청자에게 통지합니다.</li>
		<li>④ 운영기관은 관계 법령 및 내부 운영정책에 따라 회원의 서비스 이용을 제한할 수 있습니다.</li>
	</ol>

